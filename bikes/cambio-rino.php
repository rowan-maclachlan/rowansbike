    <div class="dividor-bottom"></div>
      
    <div class="bike-content">
      <p>Rowan found his bike in his neighbour's garage, sometime in the Spring
      or Summer of 2016.  He had just had his other bike stolen - a decent but
      heavy Norco 21-speed commuter with shocks, flat bars, trigger shifters,
      and a big cushy seat.  How it was stolen is a story for another time,
      though.</p>
      <p>This bike was in bad condition when Rowan found it.  Although it was
      inside a garage when he got it, it had obviously been left out in the
      elements at too many point during its life.  It had some cool 'camo'
      bartape which was flaying and falling apart - covering up badly rusted
      and ugly little steel drop bars.  Its center-pull brakes were not rusted
      away, but they were covered in dirt and grime.  The brake levers featured
      some clunky and uninspiring skull-brakes.  At the time, I didn't even
      know these abominations existed.  The tires were the only non-original
      part on the bike when I got it - cheap 27x1 1/4" Pneu Vee tires with the
      vomit-tan sidewalls.  The rims and hubs were cheap garbage: they were out
      of true when I got it, but I was never able to get them to stay true.
      Even though this period was the beginning of my education as a mechanic,
      I have never before seen rims of such poor quality.  They felt as though
      they could have been cut in half with a butter knife.  The seat and
      seatpost, too, were cheap.  They had the old style of seat clamp, and the
      post was a mere 6" deep.  It was incredible rusted into the seat-tube,
      and it took ages and the greater part of a can of WD-40 to get it loose.
      It had a couple of cheap stem-shifters with some broken friction
      adjusters, too.  In many ways, its a small miracle the frame has been
      repurposed with such care and attention as it was.</p>
    </div>

